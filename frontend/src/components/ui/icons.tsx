/**
 * Icons — centralized SVG icon library.
 *
 * All icons are inline SVG React components for zero-latency rendering.
 * Consistent API: `size` (default 18), `className` passthrough.
 */

import type { SVGProps } from "react";

interface IconProps extends SVGProps<SVGSVGElement> {
  size?: number;
}

function icon(d: string, opts?: { strokeWidth?: number; viewBox?: string }) {
  const sw = opts?.strokeWidth ?? 2;
  const vb = opts?.viewBox ?? "0 0 24 24";
  return function Icon({ size = 18, className = "", ...rest }: IconProps) {
    return (
      <svg
        xmlns="http://www.w3.org/2000/svg"
        width={size}
        height={size}
        viewBox={vb}
        fill="none"
        stroke="currentColor"
        strokeWidth={sw}
        strokeLinecap="round"
        strokeLinejoin="round"
        className={className}
        {...rest}
      >
        <path d={d} />
      </svg>
    );
  };
}

